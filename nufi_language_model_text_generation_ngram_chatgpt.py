# Function to generate each plot
def sine_wave():
    x = np.linspace(0, 10, 100)
    y = np.sin(x)
    return go.Scatter(x=x, y=y, mode='lines', name="sin(x)")

def cosine_wave():
    x = np.linspace(0, 10, 100)
    y = np.cos(x)
    return go.Scatter(x=x, y=y, mode='lines', name="cos(x)")

def tangent_wave():
    x = np.linspace(-np.pi/2, np.pi/2, 100)
    y = np.tan(x)
    return go.Scatter(x=x, y=y, mode='lines', name="tan(x)")

def exponential_curve():
    x = np.linspace(0, 5, 100)
    y = np.exp(x)
    return go.Scatter(x=x, y=y, mode='lines', name="exp(x)")

def logarithmic_curve():
    x = np.linspace(0.1, 10, 100)  # Avoid log(0)
    y = np.log(x)
    return go.Scatter(x=x, y=y, mode='lines', name="log(x)")

# Main function to update the subplot based on user selection
def generate_subplots(selected_plots):
    fig = make_subplots(rows=2, cols=3,
                        subplot_titles=selected_plots,
                        specs=[[{"type": "scatter"}]*3, [{"type": "scatter"}]*3])

    plot_mapping = {
        "Sine Wave": sine_wave(),
        "Cosine Wave": cosine_wave(),
        "Tangent Wave": tangent_wave(),
        "Exponential Curve": exponential_curve(),
        "Logarithmic Curve": logarithmic_curve(),
    }

    row, col = 1, 1
    for plot in selected_plots:
        fig.add_trace(plot_mapping[plot], row=row, col=col)
        col += 1
        if col > 3:
            col = 1
            row += 1

    fig.update_layout(title_text="Interactive Multiple Plots", height=700, width=1000)
    return fig

