def model_generation_ngram(nufi_clean, nufi_tokens, initial_text="Ngɑ̌ yú' mɑ́ á", ngram_size=5):
    starting_seq_tuple = tuple(initial_text.split(" "))

    # Count frequency of co-occurrence
    for sentence in nufi_tokens:
        for gram in ngrams(sentence, ngram_size, pad_right=True, pad_left=True):
            w_tuple = tuple(gram[:-1])
            next_word = gram[-1]
            model_ngram[w_tuple][next_word] += 1

    # Transform counts to probabilities
    for w_tuple in model_ngram:
        total_count = float(sum(model_ngram[w_tuple].values()))
        for next_word in model_ngram[w_tuple]:
            model_ngram[w_tuple][next_word] /= total_count

    starting_seq_str = " ".join(starting_seq_tuple)
    next_word = dict(model_ngram[starting_seq_tuple])
    next_word_list = sorted(next_word, key=next_word.get, reverse=True)
    next_word_prob = [{i: round(next_word[i], 5)} for i in next_word_list]
    phrase_list_containing_starting_seq = [i for i in nufi_clean if starting_seq_str in i]

    with open(path_model_ngrams, 'wb') as file:
        pickle.dump(model_ngram, file)

    return next_word_list, next_word_prob, phrase_list_containing_starting_seq

